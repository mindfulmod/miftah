// Generates data/coverage.json: the set of distinct word forms (consonant
// skeletons, matching app.js's skeleton()) in Juz ʿAmma, so the home screen
// can show "you can read N% of Juz ʿAmma's words" from local progress.
// Run from the repo root: node scripts/build-coverage.mjs
import { readFileSync, writeFileSync } from "node:fs";

const DIACRITICS = /[ً-ْٰٓ-ٟؐ-ؚۖ-ۭـ]/g; // keep in sync with app.js
const skeleton = (s) => s.normalize("NFC").replace(DIACRITICS, "");

const juz = JSON.parse(readFileSync("data/juz.json", "utf8"));
const target = juz.juzs.find((j) => j.juz === 30);
if (!target) throw new Error("juz 30 not found in data/juz.json");

const keys = new Set();
for (const [surahNum, range] of Object.entries(target.mapping)) {
  const [from, to] = String(range).split("-").map(Number);
  const surah = JSON.parse(readFileSync(`data/surah-${surahNum}.json`, "utf8"));
  for (const ayah of surah.ayahs) {
    if (ayah.number < from || ayah.number > (to ?? from)) continue;
    for (const w of ayah.words) keys.add(skeleton(w.arabic));
  }
}

writeFileSync(
  "data/coverage.json",
  JSON.stringify({
    source: "generated by scripts/build-coverage.mjs from data/juz.json + surah files",
    label: "Juz ʿAmma",
    juz: 30,
    total: keys.size,
    keys: [...keys].sort(),
  })
);
console.log(`coverage.json: ${keys.size} distinct word forms in Juz ʿAmma`);
